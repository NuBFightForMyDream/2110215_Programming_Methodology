package Lecture00_2110215;

public class Lecture00_215_02_VariableUsage {
	// main fn
	public static void main(String[] args) {
		// classname : concatenate with Capital Letter
		System.out.println(1 + 3); // 4 as result
		
		// These a and b variable arn't recommended in this subject (too confused to understand)
		int a = 1 ; // define var. with value
		int b ; // define var. without value
		
		double FirstVariable = 1.85 ; 
		double SecondVariable ;
		// given SecondVariable as 3.23
		SecondVariable = 3.23 ; 
		
		// add value together
		double TotalValue = FirstVariable + SecondVariable ; 
		System.out.println(TotalValue + a); // 6.08
		
		// we can also add mathematical constant too
		final double PI_VALUE = 3.141592 ; // final type , value can't be changed
				// constant should name with ALL CAPITAL LETTERS !!!
		
	}
}
